package com.github.SunyaWhite.abbyy.Homework2;

public class Point {

    private final int x;
    private final int y;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Point()
    {
        x = 10;
        y = 10;
    }

    public Point (int x, int y)
    {
        this.x = x;
        this.y = y;
    }

}
